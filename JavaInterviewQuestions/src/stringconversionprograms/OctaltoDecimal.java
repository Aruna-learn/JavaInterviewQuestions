package stringconversionprograms;

public class OctaltoDecimal {

	public static void main(String[] args) {
		
		System.out.println(Integer.parseInt("123", 8));
		System.out.println(Integer.parseInt("12", 8));
		System.out.println(Integer.parseInt("1", 8));
		System.out.println(Integer.parseInt("7", 8));
		

	}

}
