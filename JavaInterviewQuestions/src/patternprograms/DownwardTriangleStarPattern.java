package patternprograms;

public class DownwardTriangleStarPattern {

	public static void main(String[] args) {
		
// input 
//		******
//		*****
//		****
//		***
//		**
//		*
//		
//		
		
		
		
		for(int i=1;i<=6;i++)
		{
			for(int j=6;j>=i;j--)
			{
				System.out.print('*');
			}
			System.out.println();
		}
		
		//output is
//		******
//		*****
//		****
//		***
//		**
//		*
		
		
		
		
		
		
		
		
		
		
		

	}

}
